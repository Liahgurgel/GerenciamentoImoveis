        public void preencheImovel() {
        ImovelDAO c = new ImovelDAO();
        ArrayList<Imovel> y = new ArrayList<Imovel>();
        try {
            y = c.retornaTodos();
            for (Imovel u : y) {
                cbmImovel.addItem(u);
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmImovel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
        
    public void preencheInquilino() {
        InquilinoDAO c = new InquilinoDAO();
        ArrayList<Inquilino> y = new ArrayList<Inquilino>();
        try {
            y = c.retorna();
            for (Inquilino u : y) {
                cbmInquilino.addItem(u);
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmImovel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }


