    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (txtDataFim.getText().isEmpty() || txtDataInicio.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Favor conferir se todos os campos estão preenchidos corretamente!");
        } else {
            try {
                    Locacao locacao = new Locacao();                   
                    Imovel imovel = new Imovel();
                    imovel = (Imovel) cbmImovel.getSelectedItem();
                    Inquilino inquilino = new Inquilino();
                    inquilino = (Inquilino) cbmInquilino.getSelectedItem();
                    locacao.setImovelId(imovel.getId());
                    locacao.setInquilinoId(inquilino.getId());
                   DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
                    LocalDate dataInicio = LocalDate.parse(txtDataInicio.getText(), formatter);
                    Date dataInicioConvertida = Date.valueOf(dataInicio);
                    locacao.setDataInicio(dataInicioConvertida);
                    LocalDate dataFim = LocalDate.parse(txtDataFim.getText(), formatter);
                    Date dataFimConvertida = Date.valueOf(dataFim);
                    locacao.setDataFim(dataFimConvertida);
                    locacao.setDataInicio(dataInicioConvertida);
                   
                    LocacaoDAO locacaoDAO = new LocacaoDAO();
                    locacaoDAO.adiciona(locacao);               
                // Exibindo mensagem de sucesso
                JOptionPane.showMessageDialog(rootPane, "Locação cadastrada com sucesso!");
            } catch (SQLException ex) {
                // Exibindo mensagem de erro
                JOptionPane.showMessageDialog(rootPane, "Erro ao cadastrar o Locacao: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

